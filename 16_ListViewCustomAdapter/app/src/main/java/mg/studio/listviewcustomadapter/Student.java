package mg.studio.listviewcustomadapter;


class Student {
    private String name;
    private int studentId;

    //Setters
    public Student(String name, int studentId) {
        this.name = name;
        this.studentId = studentId;
    }

    //Getters
    public String getName() {
        return name;
    }

    public int getStudentId() {
        return studentId;
    }


}
